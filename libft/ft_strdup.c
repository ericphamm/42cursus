#include "libft.h"

static char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(const char *src)
{
	char	*dest;

	dest = malloc((ft_strlen(src)+1) * sizeof(char));
	if (dest == NULL)
	{
		return (NULL);
	}
	else
	{
		return (ft_strcpy(dest, (char *)src));
	}
}
